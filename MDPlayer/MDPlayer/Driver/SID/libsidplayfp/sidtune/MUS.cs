            info.m_loadAddr = SIDTUNE_MUS_DATA_ADDR;

            //Ptr<byte> spPet = new Ptr<byte>(musBuf, (Int32)(fileOffset + musBuf.Length - fileOffset));
            SmartPtr_sidtt<byte> spPet = new SmartPtr_sidtt<byte>(musBuf, (UInt64)(musBuf.Length - fileOffset));

            // Voice3Index now is offset to text lines (uppercase Pet-strings).
            spPet.opePlusEquel((UInt64)voice3Index);

            // Extract credits
            while (spPet.opePtr() != null)
            {
                Ptr<byte> p = spPet.opePtr();
                info.m_commentString.Add(petsciiToAscii(ref p));
            }

            spPet.opePlusPlus();

            // If we appear to have additional data at the end, check is it's
            // another mus file (but only if a second file isn't supplied)
            bool stereo = false;
            if (strBuf.Length != 0)
            {
                if (!detect(strBuf, ref voice3Index))
                    throw new loadError(ERR_2ND_INVALID);
                spPet.setBuffer(strBuf, (UInt64)strBuf.Length);
                stereo = true;
            }
            else
            {
                // For MUS + STR via stdin the files come combined
                if (spPet.good())
                {
                    UInt64 pos = spPet.tellPos();
                    if (detect(spPet.opeDKakko(0),ref voice3Index))
                    {
                        musDataLen = (UInt16)pos;
                        stereo = true;
                    }
                }
            }

            if (stereo)
            {
                // Voice3Index now is offset to text lines (uppercase Pet-strings).
                spPet.opePlusEquel((UInt64)voice3Index);

                // Extract credits
                while (spPet.opePtr()!=null)
                {
                    Ptr<byte> p = spPet.opePtr();
                    info.m_commentString.Add(petsciiToAscii(ref p));
                }

                info.m_sidChipAddresses.Add(SIDTUNE_SID2_BASE_ADDR);
                info.m_formatString = TXT_FORMAT_STR;
            }
            else
            {
                info.m_formatString = TXT_FORMAT_MUS;
            }

            setPlayerAddress();

            // Remove trailing empty lines.
            int lines = info.m_commentString.Count;
            {
                for (int line = lines - 1; line >= 0; line--)
                {
                    if (info.m_commentString[line].Length == 0)
                        info.m_commentString.RemoveAt(info.m_commentString.Count - 1);
                    else
                        break;
                }
            }
        }

    }

