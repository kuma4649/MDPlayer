        private bool mergeParts(ref byte[] musBuf, ref byte[] strBuf)
        {
            UInt32 mergeLen = (UInt32)(musBuf.Length + strBuf.Length);

            // Sanity check. I do not trust those MUS/STR files around.
            UInt32 freeSpace = (UInt32)(sidendian.endian_16(player1[1], player1[0]) - SIDTUNE_MUS_DATA_ADDR);
            if ((mergeLen - 4) > freeSpace)
            {
                throw new loadError(ERR_SIZE_EXCEEDED);
            }

            if (strBuf.Length != 0 && info.getSidChips() > 1)
            {
                // Install MUS data #2 _NOT_ including load address.
                //musBuf.AddRange(strBuf);
                List<byte> b = new List<byte>(musBuf);
                List<byte> c = new List<byte>(strBuf);
                b.AddRange(c);
                musBuf = b.ToArray();
            }

            strBuf = null;

            return true;
        }

        /**
         * Replace useless SID reads with NOPs.
         */
        private void removeReads(ref sidmemory mem, UInt16 dest)
        {
            Int32 sid_read_offset = 0x424 - o65headersize - 2;
            mem.fillRam((UInt16)(dest + sid_read_offset), 0xea, 12);
        }

        private void installPlayer(ref sidmemory mem)
        {
            // Install MUS player #1.
            UInt16 dest = sidendian.endian_16(player1[1], player1[0]);

            mem.fillRam(dest, new Ptr<byte>(player1.buf, player1.ptr + 2), (UInt32)(player1size - 2));
            removeReads(ref mem, dest);
            // Point player #1 to data #1.
            mem.writeMemByte((UInt16)(dest + 0xc6e), (SIDTUNE_MUS_DATA_ADDR + 2) & 0xFF);
            mem.writeMemByte((UInt16)(dest + 0xc70), (SIDTUNE_MUS_DATA_ADDR + 2) >> 8);

            if (info.getSidChips() > 1)
            {
                // Install MUS player #2.
                dest = sidendian.endian_16(player2[1], player2[0]);
                mem.fillRam(dest, new Ptr<byte>(player2.buf, player2.ptr + 2), (UInt32)(player2size - 2));
                removeReads(ref mem, dest);
                // Point player #2 to data #2.
                mem.writeMemByte((UInt16)(dest + 0xc6e), (byte)((SIDTUNE_MUS_DATA_ADDR + musDataLen + 2) & 0xFF));
                mem.writeMemByte((UInt16)(dest + 0xc70), (byte)((SIDTUNE_MUS_DATA_ADDR + musDataLen + 2) >> 8));
            }
        }

        public SidTuneBase load(byte[] musBuf, bool init = false)
        {
            byte[] empty = new byte[0];
            return load(musBuf, empty, 0, init);
        }

        public SidTuneBase load(byte[] musBuf, byte[] strBuf, UInt32 fileOffset, bool init = false)
        {
            UInt32 voice3Index = 0;
            if (!detect(new Ptr<byte>(musBuf.ToArray(),(Int32)fileOffset), ref voice3Index))
                return null;

            MUS tune = new MUS();
            tune.tryLoad(ref musBuf, ref strBuf, fileOffset, voice3Index, init);
            tune.mergeParts(ref musBuf, ref strBuf);

            return tune;
        }

