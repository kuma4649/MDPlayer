        private bool detect(byte[] buffer, ref UInt32 voice3Index)
        {
            if (buffer == null) return false;

            // Skip load address and 3x length entry.
            UInt32 voice1Index = 2 + 3 * 2;
            // Add length of voice 1 data.
            voice1Index += sidendian.endian_little16(new Ptr<byte>(buffer, 2));
            // Add length of voice 2 data.
            UInt32 voice2Index = voice1Index + sidendian.endian_little16(new Ptr<byte>(buffer, 4));
            // Add length of voice 3 data.
            voice3Index = voice2Index + sidendian.endian_little16(new Ptr<byte>(buffer, 6));

            return ((sidendian.endian_big16(new Ptr<byte>(buffer, (Int32)(voice1Index - 2))) == SIDTUNE_MUS_HLT_CMD)
                    && (sidendian.endian_big16(new Ptr<byte>(buffer, (Int32)(voice2Index - 2))) == SIDTUNE_MUS_HLT_CMD)
                    && (sidendian.endian_big16(new Ptr<byte>(buffer, (Int32)(voice3Index - 2))) == SIDTUNE_MUS_HLT_CMD));
        }

        private bool detect(Ptr<byte> buffer, ref UInt32 voice3Index)
        {
            if (buffer == null) return false;

            // Skip load address and 3x length entry.
            UInt32 voice1Index = 2 + 3 * 2;
            // Add length of voice 1 data.
            voice1Index += sidendian.endian_little16(new Ptr<byte>(buffer.buf,buffer.ptr+ 2));
            // Add length of voice 2 data.
            UInt32 voice2Index = voice1Index + sidendian.endian_little16(new Ptr<byte>(buffer.buf, buffer.ptr + 4));
            // Add length of voice 3 data.
            voice3Index = voice2Index + sidendian.endian_little16(new Ptr<byte>(buffer.buf, buffer.ptr + 6));

            return ((sidendian.endian_big16(new Ptr<byte>(buffer.buf, buffer.ptr + (Int32)(voice1Index - 2))) == SIDTUNE_MUS_HLT_CMD)
                    && (sidendian.endian_big16(new Ptr<byte>(buffer.buf, buffer.ptr + (Int32)(voice2Index - 2))) == SIDTUNE_MUS_HLT_CMD)
                    && (sidendian.endian_big16(new Ptr<byte>(buffer.buf, buffer.ptr + (Int32)(voice3Index - 2))) == SIDTUNE_MUS_HLT_CMD));
        }

